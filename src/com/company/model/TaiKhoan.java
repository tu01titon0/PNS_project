    public void setTenTaiKhoan(String tenTaiKhoan) {
      TenTaiKhoan = tenTaiKhoan;
    }

    public Double getSoDu() {
      return SoDu;
    }

    public void setSoDu(Double soDu) {
      SoDu = soDu;
    }

